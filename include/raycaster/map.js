export function isSolid(x, y) {
  const mx = x | 0;
  const my = y | 0;
  if (mx < 0 || mx >= MAP_WIDTH || my < 0 || my >= MAP_HEIGHT) return true;
  return MAP_DATA[my * MAP_WIDTH + mx] > 0;
}

export function canMove(x, y, radius) {
  const r = radius || 0.2;
  return !isSolid(x - r, y - r) &&
         !isSolid(x + r, y - r) &&
         !isSolid(x - r, y + r) &&
         !isSolid(x + r, y + r);
}

